#!/bin/bash
# run_tests.sh

echo "========================================="
echo "Запуск автоматизированных тестов"
echo "========================================="

# Запуск тестов с подробным выводом
python manage.py test tests/ --verbosity=2

# Запуск с оценкой покрытия кода (если установлен coverage)
if command -v coverage &> /dev/null; then
    echo ""
    echo "========================================="
    echo "Оценка покрытия кода тестами"
    echo "========================================="
    
    coverage run manage.py test tests/
    coverage report -m
    coverage html
    
    echo "Отчёт о покрытии создан в директории htmlcov/"
fi

echo ""
echo "========================================="
echo "Тестирование завершено"
echo "========================================="